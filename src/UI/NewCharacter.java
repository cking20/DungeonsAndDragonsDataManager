    private void classComboBoxPropertyChange(java.beans.PropertyChangeEvent evt) {//GEN-FIRST:event_classComboBoxPropertyChange
        // TODO add your handling code here:
        //class selected, load the json file, populate text area
        classDescriptionArea.setText("");
        //classComboBox.item
        String selected = (String)classComboBox.getSelectedItem();
        System.out.println(selected);
        String theJSON;
        if(selected.compareTo("Item 1") != 0){
            theJSON = Data.IO.LoadJSONFile(Data.IO.classDir+"/"+selected+".ser");
            Data.ClassArchtype classs = Data.DataCollection.FilterClassArchTypeJSON(theJSON);
            String choices = "";
            String profis = "";
            String saving = "";
             for(int i = 0; i < classs.savingThrows.size(); i++){
                saving+="   "+classs.savingThrows.get(i).name+"\n";
            }
            for(int i = 0; i < classs.proficiencies.size(); i++){
                profis+="   "+classs.proficiencies.get(i).name+"\n";
            }
            for(int i = 0; i < classs.profcncyChoices.size(); i++){
                choices+="   "+classs.profcncyChoices.get(i).name+"\n";
            }
            classDescriptionArea.setText(
                    "Hit Die: "+classs.hitDie+"\n\n"+
                    "Choices: Choose "+classs.numProfcncyToChoose+"\n"+choices+'\n'+
                    "Proficiencies:\n"+profis+"\n"+
                    "Saving Throws:\n"+saving+"\n"
            );
            
                    
        }
        
        //Data.IO.LoadJSONFile(Data.IO.classDir+"/"+selected+".ser");
    }//GEN-LAST:event_classComboBoxPropertyChange

    private void raceComboBoxPropertyChange(java.beans.PropertyChangeEvent evt) {//GEN-FIRST:event_raceComboBoxPropertyChange
        // TODO add your handling code here:
        raceDescriptionArea.setText("");
        //classComboBox.item
        String selected = (String)raceComboBox.getSelectedItem();
        System.out.println(selected);
        String theJSON;
        if(selected.compareTo("Item 1") != 0){
            theJSON = Data.IO.LoadJSONFile(Data.IO.raceDir+"/"+selected+".ser");
            Data.Race race = Data.DataCollection.FilterRaceJSON(theJSON);
           
            raceDescriptionArea.setText(
                    "Alignment: \n   "+race.alignment+"\n"+
                    "Size: \n   "+race.size+"\n"+
                    "Speed: \n   "+race.speed+"\n"+
                    "Age: \n   "+race.alignment+"\n");
            
            raceDescriptionArea.setText(raceDescriptionArea.getText()+
                    "Traits: \n");
            for(int i = 0; i < race.traits.size(); i++)        
                raceDescriptionArea.setText(raceDescriptionArea.getText()+
                        "   "+race.traits.get(i).name+"\n");
            
            raceDescriptionArea.setText(raceDescriptionArea.getText()+
                    "Proficiencies: \n");
            for(int i = 0; i < race.starting_proficiencies.size(); i++)        
                raceDescriptionArea.setText(raceDescriptionArea.getText()+
                        "   "+race.starting_proficiencies.get(i).name+"\n");
            
            raceDescriptionArea.setText(raceDescriptionArea.getText()+
                    "Proficiency Choices: "+"Choose "+race.choose);
            for(int i = 0; i < race.from.size(); i++)        
                raceDescriptionArea.setText(raceDescriptionArea.getText()+
                        "   "+race.from.get(i).name+"\n");
            
            raceDescriptionArea.setText(raceDescriptionArea.getText()+
                            "Languages:\n"+race.language_desc+"\n"); 
            for(int i = 0; i < race.languages.size(); i++)        
                raceDescriptionArea.setText(raceDescriptionArea.getText()+
                        "   "+race.languages.get(i).name+"\n");       
        }
    }//GEN-LAST:event_raceComboBoxPropertyChange

    
    private ComboBoxModel<String> PopulateClasses(){
        DefaultComboBoxModel<String> listModel = new DefaultComboBoxModel<>();
        ArrayList<String> fn = Data.IO.GetFileNames(Data.IO.classDir);
        for (int i = 0; i < fn.size(); i++) {
            if(fn.get(i).contains(".ser"))
                listModel.addElement(fn.get(i).replace(".ser", ""));    
        }        
        return listModel;
    }
    private ComboBoxModel<String> PopulateRaces(){
        DefaultComboBoxModel<String> listModel = new DefaultComboBoxModel<>();
        ArrayList<String> fn = Data.IO.GetFileNames(Data.IO.raceDir);
        for (int i = 0; i < fn.size(); i++) {
            listModel.addElement(fn.get(i).replace(".ser", ""));    
        }        
        return listModel;
    }
