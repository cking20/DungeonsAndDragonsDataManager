    private void finalizeCharacterButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_finalizeCharacterButtonActionPerformed
        // TODO add your handling code here:
        cs = new CharacterSheet();
        String name = newCharNameField.getText();
        String classs = classComboBox.getItemAt(classComboBox.getSelectedIndex());
        String race = raceComboBox.getItemAt(raceComboBox.getSelectedIndex());
        if(name.compareTo("") == 0)
            return;      
        if(classs.compareTo("") == 0)
            return;
        if(race.compareTo("") == 0)
            return;
        cs.name = name;
        cs.race = race;
        cs.classs = classs;
        //Main.playerActiveChar = cs;
        returnTo.AddPlayerCharacter(cs);
        this.dispose();
                
    }//GEN-LAST:event_finalizeCharacterButtonActionPerformed

