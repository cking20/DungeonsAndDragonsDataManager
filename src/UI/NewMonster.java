    public void RefreshAbilityList(){
        DefaultListModel<String> abs = new DefaultListModel<>();
            for(int i = 0; i < monsterActiveChar.actions.size(); i++){
                Data.Action a = monsterActiveChar.actions.get(i);
                abs.addElement(a.name+": "+a.attack_bonus+"   "+a.damage_dice);
            }
            for(int i = 0; i < monsterActiveChar.special_abilities.size(); i++){
                Data.Action a = monsterActiveChar.special_abilities.get(i);
                abs.addElement(a.name+": "+a.attack_bonus+"   "+a.damage_dice);
            }
            for(int i = 0; i < monsterActiveChar.legendary_actions.size(); i++){
                Data.Action a = monsterActiveChar.legendary_actions.get(i);
                abs.addElement(a.name+": "+a.attack_bonus+"   "+a.damage_dice);
            }
            abilityList.setModel(abs);
    }
