    private int CalcModValue(int base){
        return (base - 10) >> 1;
    }
    private String ActionListToJSON(ArrayList<Data.Action> a){
        String ActionJSONTemplate = "{\"damage_dice\":\"__DMG_DICE__\","
                + "\"damage_bonus\":\"__DMG_BONUS__\","
                + "\"attack_bonus\":__ATTACKBONUS__,"
                + "\"desc\":\"__DESC__\","
                + "\"name\":\"__NAME__\"}";
        String ret = "";
        for (int i = 0; i < a.size(); i++) {
            Data.Action e = a.get(i);
            ret = ret + ActionJSONTemplate;
            ret = ret.replace("__DMG_DICE__", e.damage_dice);
            ret = ret.replace("__DMG_BONUS__", e.damage_bonus+"");
            ret = ret.replace("__ATTACKBONUS__", e.attack_bonus+"");
            ret = ret.replace("__DESC__", e.desc+"");
            ret = ret.replace("__NAME__", e.name+"");
            ret = ret + ",";
        }
        if(a.size() > 0)
            ret = ret.substring(0, ret.length()-1);
        return ret;
    }
    
